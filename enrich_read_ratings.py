def main():
    with open('data/goodreadsData.json') as f:
        data = json.load(f)

    updated = 0
    skipped = 0
    for book in data['books']:
        if book['shelf'] not in ('read', 'currently-reading'):
            continue
        key = book['title']
        if key in LOOKUP:
            avg, count = LOOKUP[key]
            if book.get('avgRating', 0) == 0:
                book['avgRating'] = avg
            if book.get('ratingsCount', 0) == 0:
                book['ratingsCount'] = count
            updated += 1
        else:
            skipped += 1

    with open('data/goodreadsData.json', 'w') as f:
        json.dump(data, f, indent=2)
        f.write('\n')

    print(f"Updated: {updated} books")
    print(f"Skipped (not in lookup): {skipped} books")

if __name__ == '__main__':
    main()
